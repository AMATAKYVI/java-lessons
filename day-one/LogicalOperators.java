public class LogicalOperators {
    public static void main(String[] args) {
        // ! this mean negative
        // \ || \ this is the or operator
        // \ > \ this is the greater than
        // \ >= \ this is the greater than or equal to
        // \ < \ this is the less than
        // \ <= \ this is the less than or equal to
        // \ && \ this is an and operator



        int a = 12;
        int b = 1;
        if (a != b) {
            System.out.println("this is not equal");
        } else {
            System.out.println("this is equal");
        }
    }
}
