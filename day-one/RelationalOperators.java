public class RelationalOperators {

    public static void main(String[] args) {
        // == is a relational operator you want to compare to value or more if it equal to each
        // other

        int a = 12;
        int b = 1;
        // comparing two integer number together if it equal
        if (a == b)
            System.out.println("a and b are equal");
        else
            System.out.println("a and b are not equal");
    }
}
